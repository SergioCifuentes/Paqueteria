private void obtenerCLientes(){
    clientes= new ArrayList<>();
ArrayList codigos = ControladorDB.obtenerCodigoDeClientes();
    for (int i = 0; i < codigos.size(); i++) {
        clientes.add(ControladorDB.verificarClientePorCodigo((Integer)codigos.get(i)));        
    }
}
    private void mostrarRutasEnTabla() {
        if (clientes.size() > 0) {
            DefaultTableModel model = (DefaultTableModel) tblClientes.getModel();
            int aux = model.getRowCount();
            for (int i = aux; i > 0; i--) {
                model.removeRow(i - 1);
            }
            for (int i = 0; i < clientes.size(); i++) {//Creacion de Celdas
                model.addRow(new Object[]{"", "", "", "", "", ""});
                tblClientes.setValueAt(clientes.get(i).getCodigo(), i, 0);
                if (clientes.get(i).getNit()==0) {
                    tblClientes.setValueAt("C/F", i, 1);
                } else {
                    tblClientes.setValueAt(clientes.get(i).getNit(), i, 1);
                }

                tblClientes.setValueAt(clientes.get(i).getNombre(), i, 2);
                ArrayList<Paquete> paquetes = TransferenciasDB.obtenerPaquetesPorCliente(clientes.get(i).getCodigo());
                tblClientes.setValueAt(paquetes.size(), i, 3);
                float ganacias=0;
                float perdidas=0;
                for (int j = 0; j < paquetes.size(); j++) {
                    ganacias=ganacias+paquetes.get(j).getPrecioPagado();
                    perdidas=perdidas+paquetes.get(j).getPrecioPerdido();
                }
                tblClientes.setValueAt("$"+String.format("%6.2f",ganacias), i, 4);
                tblClientes.setValueAt("$"+String.format("%6.2f",perdidas), i, 5);
                tblClientes.setValueAt("$"+String.format("%6.2f",ganacias-perdidas), i, 6);
            }
        } else {
            lblError.setVisible(true);
        }
    }
