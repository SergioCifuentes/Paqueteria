    private void buscar(String palabra) {
        if (!"".equals(palabra)) {
            ArrayList<Cliente> aux = new ArrayList<>();
            for (int i = 0; i < clientes.size(); i++) {
                if (String.valueOf(clientes.get(i).getCodigo()).startsWith(palabra)) {
                    aux.add(clientes.get(i));
                }
            }
            boolean auxiliar;
            for (int i = 0; i < clientes.size(); i++) {
                auxiliar = true;
                for (int j = 0; j < aux.size(); j++) {
                    if (clientes.get(i).getCodigo() == aux.get(j).getCodigo()) {
                        auxiliar = false;
                    }
                }
                if (auxiliar) {
                    aux.add(clientes.get(i));
                }
            }
            clientes = aux;
        }

    }

